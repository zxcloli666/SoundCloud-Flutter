//! Диагностика доступности: «доступно ли вообще, или режут только меня?».
//!
//! Отвечает однозначным [`Reachability`], а не догадкой: различает «лежит у
//! всех» и «лежит только у меня» через внешний консенсус (check-host.net) — на
//! этом строится решение включать пробив ([`sc-net-bypass`](../sc_net_bypass)).
//! Весь трафик идёт через [`sc-net`](../sc_net), поэтому диагностика сама умеет
//! ходить через прокси/пробив.

mod prober;

pub use prober::Prober;

/// Однозначный вердикт о доступности цели.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum Reachability {
    /// Доступно напрямую.
    Online,
    /// Отвечает, но нестабильно / консенсус неясен.
    Degraded,
    /// Лежит только для нас — внешний мир видит сервис живым (→ включать пробив).
    BlockedLocally,
    /// Лежит у всех.
    Down,
    /// Нет сети вообще.
    Offline,
}
